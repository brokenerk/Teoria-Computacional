    public void concatena(String alfpotencia[], char a[], int num, int len, boolean neg){
        if(num==0){
            String finl="{ λ, ";
            
            
            for(int x=0; x<alfpotencia.length; x++){
                if(x==alfpotencia.length-1){
                    finl=finl+alfpotencia[x]+" }";
                }
                else{
                     finl=finl+alfpotencia[x]+", ";
                }
            }
            TxtΣ.setText(finl);
        }
        else{
            int k=0;
            String aux[]=new String[alfpotencia.length];
            for(int i=0; i<len; i++){
                for(int j=0; j<a.length; j++){
                    
                    if(neg==true){
                        aux[k]=a[j]+alfpotencia[i];
                    }
                    else{
                        aux[k]=alfpotencia[i]+a[j];
                    }
                     k++;
                }
            }
            concatena(aux, a, num-1, k, neg);
        }
    }
    
