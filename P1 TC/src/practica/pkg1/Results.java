    private void BtnCadenasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadenasActionPerformed
     String cadena1,cadena2;
     int r=1;
     
    
     cadena1=JOptionPane.showInputDialog(null,"Ingrese la cadena w1:","Cadena w1", JOptionPane.QUESTION_MESSAGE);
     r=estaEnAlf1(cadena1, LblAux1.getText());
     
     while (r==0){
           cadena1=JOptionPane.showInputDialog(null,"Cadena inválida. Ingrese otra cadena w1:","Error", JOptionPane.ERROR_MESSAGE);
         r=estaEnAlf1(cadena1, LblAux1.getText());
     }
     
     if(cadena1.length()==0) TxtCad1.setText("λ");
     else TxtCad1.setText(cadena1);
     
     
     cadena2=JOptionPane.showInputDialog(null,"Ingrese la cadena w2:","Cadena w2", JOptionPane.QUESTION_MESSAGE);
     r=estaEnAlf1(cadena2, LblAux1.getText());
     
     while (r==0){
         cadena2=JOptionPane.showInputDialog(null,"Cadena inválida. Ingrese otra cadena w2:","Error", JOptionPane.ERROR_MESSAGE);
         r=estaEnAlf1(cadena2, LblAux1.getText());
     }

     if(cadena2.length()==0) TxtCad2.setText("λ");
     else TxtCad2.setText(cadena2);
     
     
     if((TxtCad1.getText()).equals("λ")){
         TxtSub.setText("es un PREFIJO/SUFIJO IMPROPIO y SUBCADENA");
     }
     else{
        int sub=cadena2.indexOf(cadena1);
        char c1[]=cadena1.toCharArray();
        char c2[]=cadena2.toCharArray();
        int indice1=cadena1.length()-1;
        int indice2=cadena2.length()-1;
        if(sub!=-1){
            if(cadena2.equals(cadena1)){
                 TxtSub.setText("es un PREFIJO/SUFIJO IMPROPIO y SUBCADENA");
            }
            else if(c2[0]==c1[0] && c2[2]==c1[1]){
                 TxtSub.setText("es un PREFIJO PROPIO"); 
            }
            else if (c2[cadena2.length()-1]==c1[cadena1.length()-1] && c2[cadena2.length()-2]==c1[cadena1.length()-2]){
                TxtSub.setText("es un SUFIJO PROPIO"); 
            }
            else{
                TxtSub.setText("es una SUBCADENA"); 
            }
        }
        else{
             TxtSub.setText("NO ES SUBCADENA"); 
        }
     }
     TxtX.setEnabled(true);
     TxtX.setEditable(true);
     TxtSub.setEnabled(true);
     TxtCad1.setEnabled(true);
     TxtCad2.setEnabled(true);
     BtnNw1w2.setEnabled(true);
     BtnX.setEnabled(true);
    }//GEN-LAST:event_BtnCadenasActionPerformed

